package day1;

public class Arithmetic_Operators {
    public static void main(String[] args) {
        int x =10;
        int y = 20;
        //Addition
        int sum= x+y;
        System.out.println(sum);
        //Subtraction
        int sub= y-x;
        System.out.println(sub);
        //Multiplication
        int multi= x*y;
        System.out.println(multi);
        //Division
        int division= x/y;
        System.out.println(division);
        //Modulus
        int Modulus= x%y;
        System.out.println(Modulus);
    }

}
