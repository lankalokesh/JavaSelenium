package day1;

public class Datatypes {

    public static void main(String[] args) {
    //primitive data types//
        int age;
        age=40;
        System.out.println(age);
        System.out.println("age");
        //
        float weight=80.45f;
        System.out.println(weight);
        //long datatype is used for storing a long number, if the value is less than 10 digits l is not required //
        long aadhaar = 76989898545657l;
        System.out.println(aadhaar);
        short pin = 23456;
        System.out.println(pin);
        double rate = 4353435454654.89789;
        System.out.println(rate);
        char ch = '@';
        System.out.println(ch);
        boolean wfh = true;
        System.out.println(wfh);
    }
}
